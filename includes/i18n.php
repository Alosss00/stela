if (!function_exists('stela_get_server_language')) {
    function stela_get_server_language() {
        $lang = isset($_COOKIE['language']) ? strtolower(trim($_COOKIE['language'])) : 'id';
        return in_array($lang, ['id', 'en'], true) ? $lang : 'id';
    }
}

